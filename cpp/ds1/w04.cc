#include <iostream>

using namespace std;

int main() {
  // 기본 초기화: 값이 지정되지 않으면 0으로 초기화
  int a{};
  cout << "기본초기화: " << a << "\n";

  // 직접 초기화: 등호 사용
  int b = 42;
  cout << "직접초기화: " << b << "\n";

  // 유니폼 초기화: 중괄호 사용
  // 유니폼 초기화는 타입 변환시 좁히기 변환을 방지함
  // int x{3.14}; 같은 코드는 컴파일 오류를 발생시킴
  // 가장 최신 트렌드
  double c{3.14};
  cout << "유니폼초기화: " << c << "\n";

  // 생성자 초기화: 괄호 사용
  int d(10);
  cout << "생성자초기화: " << d << "\n";

  // 배열의 유니폼 초기화
  int arr[]{1, 2, 3, 4, 5};
  cout << "배열 첫번째 값: " << arr[0] << "\n";
  cout << "배열 마지막 값: " << arr[4] << "\n";

  return 0;
}